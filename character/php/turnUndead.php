function turnHd1 ($level)
{
    $turn = "";
    
    if($level == 1)
    {
        $turn = "13";
    }
    else if($level == 2)
    {
        $turn = "11";
    }
    else if($level == 3)
    {
        $turn = "9";
    }
    else if($level == 4)
    {
        $turn = "7";
    }
    else if($level == 5)
    {
        $turn = "5";
    }
    else if($level == 6)
    {
        $turn = "3";
    }
    else if($level == 7)
    {
        $turn = "2";
    }
    else if($level >=8 && $level <= 10)
    {
        $turn = "T";
    }
    else if($level >10)
    {
        $turn = "D";
    }
    else
    {
        $turn = "-";
    }
    
    return $turn;
    
}


